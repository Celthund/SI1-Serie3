public class Pessoa {
    final public int numero;
    final public String nome, endereco, ntelefone, email;

    public Pessoa(int numero, String nome, String endereco, String ntelefone, String email) {
        this.numero = numero;
        this.nome = nome;
        this.endereco = endereco;
        this.ntelefone = ntelefone;
        this.email = email;
    }
}
