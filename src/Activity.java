public class Activity {
    final public int referencia, duracao;
    final public String designacao, descricao, participacao;

    public Activity(int referencia, int duracao, String designacao, String descricao, String participacao) {
        this.referencia = referencia;
        this.duracao = duracao;
        this.designacao = designacao;
        this.descricao = descricao;
        this.participacao = participacao;
    }
}
